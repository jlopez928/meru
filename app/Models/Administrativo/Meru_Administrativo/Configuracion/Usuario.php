<?php

namespace App\Models\Administrativo\Meru_Administrativo\Configuracion;

use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use App\Enums\Administrativo\Meru_Administrativo\Estado;
use App\Models\Administrativo\Meru_Administrativo\Compras\Comprador;
use App\Models\Administrativo\Meru_Administrativo\Configuracion\Gerencia;

class Usuario extends Model
{
	use HasFactory;

	protected $table      	= 'usuarios';

	protected $dateFormat 	= 'd/m/Y H:i:s';

	protected $primaryKey 	= 'usuario';

	protected $keyType 		= 'string';

	protected $guarded   	= [];

	public $timestamps 		= false;

	protected $casts 		= 	[
									'status' => Estado::class,
								];

 	////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////// ACCESORS Y MUTATORS /////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////



	////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////// RELACIONES //////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////
    public function gerencia()
    {
        return $this->belongsTo(Gerencia::class, 'cod_ger', 'cod_ger');
    }
	////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////// QUERY SCOPES ////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////
	public function scopeActivo($query)
    {
        return $query->where('status', Estado::Activo);
    }

	public function scopeNotComprador($query)
    {
        return $query->whereNotIn('usuario', Comprador::query()->pluck('usu_com'));
    }
}
